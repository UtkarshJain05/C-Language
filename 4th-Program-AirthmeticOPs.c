
#include<stdio.h>
int main()
{

// Integer Airthmetic OPs:-

    int x = 7;
    int y = 2;
    printf("%d\n", x + y);
    printf("%d\n", x - y);
    printf("%d\n", x * y);
    printf("%d\n", x / y);  // Answer will come in integer.

    int z = x / y;
    printf("%d\n",z);
    printf("\n");

// Float Airthmetic OPs:-

    float a = 5;
    float b = 4;
    printf("%f\n", a + b);
    printf("%f\n", a - b);
    printf("%f\n", a * b);
    printf("%f\n", a / b);  // Answer will come in Decimals.
    
return 0;
}

// "%d" and "%f" are combinely called Format Specifiers in C language.